trigger enterrange(0x01) {
	loc src_loc = getLocation(this);
	int x = getX(src_loc);
	int y = getY(src_loc);
	int z = getZ(src_loc);
	loc dest;
	if ((x == 0x1461) && (y == 0x0249) && (z == (0x00 - 0x0A))) {
		dest = 0x14BA, 0x0216, 0x00;
	}
	if ((x == 0x14B9) && (y == 0x0213) && (z == 0x0A)) {
		dest = 0x1461, 0x0247, 0x00;
	}
	if ((x == 0x14E4) && (y == 0x0242) && (z == (0x00 - 0x0A))) {
		dest = 0x1415, 0x028A, 0x00;
	}
	if ((x == 0x1413) && (y == 0x028A) && (z == 0x0A)) {
		dest = 0x14DE, 0x0242, 0x00;
	}
	if ((x == 0x1462) && (y == 0x02F9) && (z == (0x00 - 0x1E))) {
		dest = 0x14BA, 0x028E, 0x00;
	}
	if ((x == 0x14BA) && (y == 0x028B) && (z == 0x0A)) {
		dest = 0x1463, 0x0242, (0x00 - 0x14);
	}
	if (!teleport(target, dest)) {
		return(0x00);
	}
	return(0x01);
}
